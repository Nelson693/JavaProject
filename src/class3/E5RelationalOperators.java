package class3;

public class E5RelationalOperators {
    public static void main(String[] args) {
        System.out.println(10>15);
        System.out.println(10<15);
        System.out.println(10==15); // EQUAL TO
        System.out.println(10!=15); // NOT EQUAL TO
        System.out.println(10>=15); // IS IT GREATER or EQUAL TO (False)
        System.out.println(10<=15); //is 10 less than or equal to 15 (Will be tru becuse the first part is true)
    }



}
